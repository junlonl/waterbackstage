package com.hhh.fund.util;

public class StringUtils {

	public static boolean isBlank(String str){
		boolean flag=true;
		if(str!=null && !"".equals(str.trim())){
			flag=false;
		}
		return flag;
	}
	public static boolean isNotBlank(String str){
		boolean flag=false;
		if(str!=null && !"".equals(str.trim())){
			flag=true;
		}
		return flag;
	}
	
	public static boolean isContains(String resources,String containsStr){
		
		if(resources.indexOf(containsStr)!=-1){
			return true;
		}else{
			return false;
		}
	}
	
	
}
